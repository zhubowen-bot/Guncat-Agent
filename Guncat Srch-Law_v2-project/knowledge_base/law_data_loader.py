def load_regulation_by_name(name: str) -> List[Dict[str, Any]]:
    """
    按名称加载特定法规（扩展接口）

    Args:
        name: 法规名称

    Returns:
        该法规的法条列表
    """
    all_regs = load_core_regulations()
    return [r for r in all_regs if name in r.get("source", "")]


